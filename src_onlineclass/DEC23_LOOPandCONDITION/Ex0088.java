package src_onlineclass.DEC23_LOOPandCONDITION;

public class Ex0088 {
    public static void main(String[] args) {
// While and Do while
        int i =1; // A - Init
        while(i<=10){ // B - Cond
            System.out.println(i); // D - Code Execute
            i++; // C - I/D
        }
        for (int j=1; j<=10; j=j+2){
            System.out.println(j);
        }
    }
}
