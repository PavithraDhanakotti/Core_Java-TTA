package Core_Java.src_onlineclass.DEC10_DATATYPESandVARIABLES;

public class Ex0012 {
    public static void main(String[] args) {

        // Operators:::

        // Assignment operator(=) from right to left as it stores
        int age = 10;

        // Arithmetic operators -> +,-,*,/,%
        int age1 = 10;
        double age2 = 52;
        double sum = age1+age2;
        System.out.println(sum);
        double sub = age2-age1;
        System.out.println(sub);
        double div = age1/age2; // int will not be used as 50/10 goes in float
        System.out.println(div);
        double mul = age1*age2;
        System.out.println(mul);

    }
}
