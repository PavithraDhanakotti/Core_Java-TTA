package Core_Java.src_onlineclass.DEC17_IFAndSWITCHCondition.Ex0050;

public class Ex0061 {
    public static void main(String[] args) {
        byte b=20;
        switch(b){
            case 10:
                System.out.println("TEN");
            case 127:
                System.out.println("ONE TWENTY EIGHT");
            default:
                System.out.println("Default");
        }

    }
}
